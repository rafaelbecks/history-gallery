#!/usr/bin/env node
/**
 * Scans env/*.hdr and writes src/hdrEnvironments.generated.js
 */
import { readdirSync, writeFileSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const hdrDir = join(root, "env");
const outFile = join(root, "src/hdrEnvironments.generated.js");

function toId(stem) {
  const id = stem
    .replace(/[^a-zA-Z0-9]+/g, "_")
    .replace(/^_|_$/g, "")
    .toLowerCase();
  return /^\d+$/.test(id) ? `hdr_${id}` : id;
}

function toLabel(stem) {
  return stem.replace(/_/g, " ");
}

const files = readdirSync(hdrDir)
  .filter((f) => f.toLowerCase().endsWith(".hdr"))
  .sort((a, b) => a.localeCompare(b, undefined, { numeric: true }));

const entries = files.map((file) => {
  const stem = file.replace(/\.hdr$/i, "");
  const id = toId(stem);
  return [id, { label: toLabel(stem), file }];
});

const body = entries
  .map(([id, { label, file }]) => `  ${id}: {\n    label: ${JSON.stringify(label)},\n    file: ${JSON.stringify(file)},\n  },`)
  .join("\n");

const source = `/** Auto-generated by scripts/generate-hdr-config.mjs — do not edit. */
export const HDR_ENVIRONMENTS = {
${body}
};
`;

writeFileSync(outFile, source);
console.log(`Wrote ${entries.length} environments → ${outFile}`);
